class Solution {
  public:
    void bfs(int vertex, vector<int> adjLs[], int vis[])
    {
        queue<int> q;
        q.push(vertex);
        vis[vertex] = 1;
        while(!q.empty())
        {
            int temp = q.front();
            q.pop();
            for(auto x:adjLs[temp])
            {
                if(!vis[x])
                {
                    q.push(x);
                    vis[x] = 1;
                }
            }
        }
        return;
    }
    int numProvinces(vector<vector<int>> adj, int V) {
        
        vector<int> adjLs[V]; 
        
        // to change adjacency matrix to list 
        for(int i = 0;i<V;i++) {
            for(int j = 0;j<V;j++) {
                // self nodes are not considered
                if(adj[i][j] == 1 && i != j) {
                    adjLs[i].push_back(j); 
                    adjLs[j].push_back(i); 
                }
            }
        }
        int vis[V] = {0}; 
        int cnt = 0; 
        for(int i = 0;i<V;i++) {
            // if the node is not visited
            if(!vis[i]) {
                // counter to count the number of provinces 
                cnt++;
                bfs(i, adjLs, vis); 
            }
        }
        return cnt;
    }
};

